package AtoZAlphabeticalPattern;

public class Kkkkkkk
{
	public static void main(String args[])
	{
		int i,j;
		int n=5;
		for (i=1;i<=n;i++)
		{
			for (j=1;j<=n;j++)
			{
				if(j==3||i==3)
				{
					System.out.print(" A");
				}
				else
				{
					System.out.print(" B");
				}
			}
			System.out.print("\n");
		}
	}
}
